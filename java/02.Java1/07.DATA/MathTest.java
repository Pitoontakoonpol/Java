class MathTest {
    public static void main(String[] args) {
        System.out.println(Math.pow(2.5, 2));
        System.out.println(Math.log(3) / Math.log(10));
        System.out.println(Math.sqrt(2));
        System.out.println(Math.sin(30.0 / 180.0 * Math.PI));
        System.out.println(Math.cos(120.0 / 180.0 * Math.PI));
        System.out.println(Math.tan(390.0 / 180.0 * Math.PI));
    }
}
